package cate.model;

public class ScateBean {

	private int scatenum;
	private String bcate;
	private String scate;
	
	public ScateBean() {
		super();
	}
	
	public int getScatenum() {
		return scatenum;
	}
	public void setScatenum(int scatenum) {
		this.scatenum = scatenum;
	}
	public String getBcate() {
		return bcate;
	}
	public void setBcate(String bcate) {
		this.bcate = bcate;
	}
	public String getScate() {
		return scate;
	}
	public void setScate(String scate) {
		this.scate = scate;
	}
	
	
}
